    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(!txtDni.getText().equals("") && !txtNombre.getText().equals("") && !txtApellido.getText().equals("")){
            String mes = String.valueOf(comboMes.getSelectedIndex());
            String fec = comboA単o.getSelectedItem().toString() + "/" + mes + "/" + comboDia.getSelectedItem().toString();
            gf.setDniEmpleado(Integer.valueOf(txtDniEmpleado.getText().toLowerCase()));
            gf.setDni(Integer.parseInt(txtDni.getText().toLowerCase()));
            gf.setApellido(txtApellido.getText().toLowerCase());
            gf.setNombre(txtNombre.getText().toLowerCase());
            gf.setParentesco(comboParentesco.getSelectedItem().toString());
            gf.setFechaNac(fec);
            int disc = comboDiscapacidad.getSelectedIndex();
            if(disc==1){
                gf.setDiscapacidad(false);
                            }
                if(disc==2){
                gf.setDiscapacidad(true);
            }
                gf.actualizarGrupoFamiliar();
            dispose();
        } else{
            JOptionPane.showMessageDialog(null,"Datos Faltantes","Error",JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_btnAgregarActionPerformed

